#include <stdio.h>

/**
 * main - Displays the alphabet in lowercase and also in uppercase.
 * Return: Always 0 (Successful)
 */
int main(void)
{
char alp[52] = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ";
	int i;

	for (i = 0; i < 52; i++)
	{
		putchar(alp[i]);
	}
	putchar('\n');
	return (0);
}
